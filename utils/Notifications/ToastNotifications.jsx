import { ToastContainer, toast } from "react-toastify";
import React from "react";

export const ToastSuccessNotifications = (message) => {
  toast.success(message, {
    position: "top-right",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });

  return <div></div>;
};

